package Giris;

public class FloatveDouble {
    public static void main(String[] args) {
        float number1 = 3.14F;
        System.out.println(number1);
        float number2 = 3.14f;
        System.out.println(number2);

        double number3 = 3.14;
        System.out.println(number3);
    }
}
